if __name__ == '__main__':
    # bool类型
    a = True
    b = False

    # 比较运算符
    print(2 < 3)  # True
    print(2 == 3)  # False

    # 逻辑运算符
    print(a and b)  # False
    print(a or b)  # True
    print(not a)  # False

    # 类型转换
    print(int(a))  # 1
    print(float(b))  # 0.0
    print(str(a))  # True
